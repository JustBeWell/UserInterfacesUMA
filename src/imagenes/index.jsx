/**
 * Función que devuelve la imagen de una carta según su naipe y valor
 * @param {string} naipe - El naipe de la carta ('diamante', 'corazon', 'trebol', 'pica')
 * @param {string} valor - El valor de la carta ('A', '2', '3', ..., '10', 'J', 'Q', 'K')
 * @returns {string} - La ruta de la imagen correspondiente
 */
export const obtenerImagenCarta = (naipe, valor, alternativa) => {
	if (naipe === null || valor === null)
		return alternativa ? CartaPorDetrasAlternativa : CartaPorDetras;
	let naipeLower = naipe.toLowerCase();
	if (naipeLower === "corazon") naipeLower = "corazones"; // Normalizamos el naipe a 'corazones'
	if (naipeLower === "trebol") naipeLower = "tréboles"; // Normalizamos el naipe a 'tréboles'
	if (naipeLower === "pica") naipeLower = "picas"; // Normalizamos el naipe a 'picas'
	if (naipeLower === "diamante") naipeLower = "diamantes"; // Normalizamos el naipe a 'diamantes'
	if (valor === "As") valor = "A"; // Normalizamos el valor 'As' a 'A'
	// Verifica si el naipe está disponible
	if (alternativa) {
		if (
			imagenesNaipesAlternativa[naipeLower] &&
			imagenesNaipesAlternativa[naipeLower][valor]
		) {
			return imagenesNaipesAlternativa[naipeLower][valor];
		}
	} else {
		if (imagenesNaipes[naipeLower] && imagenesNaipes[naipeLower][valor]) {
			return imagenesNaipes[naipeLower][valor];
		}
	}
	// Si no se encuentra la carta o el naipe, devolver el reverso
	return alternativa ? CartaPorDetrasAlternativa : CartaPorDetras;
};

